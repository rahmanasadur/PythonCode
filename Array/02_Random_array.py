# random array(floatinf type)
import numpy as np
one_d = np.random.rand(3)
two_d = np.random.rand(2,3)
three_d = np.random.rand(2,3,4)
print(one_d)
print(two_d)
print(three_d)


# Int type
one_d=np.random.randint(100, size=(2))
two_d=np.random.randint(100, size=(2,3))
three_d=np.random.randint(100, size=(2,3,4))

print(one_d)
print(two_d)
print(three_d)


# Int type random in range

one_d=np.random.randint(50,100, size=(2))
